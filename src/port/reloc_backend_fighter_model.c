static void ndsFighterPartsSetIdentity(Mtx44f mtx)
{
    s32 i;
    s32 j;

    for (i = 0; i < 4; i++)
    {
        for (j = 0; j < 4; j++)
        {
            mtx[i][j] = (i == j) ? 1.0F : 0.0F;
        }
    }
}

static void ndsFighterPartsSyncDObj(FTStruct *fp, DObj *dobj, u32 joint_id)
{
    FTParts *parts;
    Vec3f *translate;
    Vec3f *scale;
    u32 slot;

    if ((fp == NULL) || (dobj == NULL) ||
        (fp->nds_slot >= GMCOMMON_PLAYERS_MAX) ||
        (joint_id >= nFTPartsJointNumMax))
    {
        return;
    }
    slot = fp->nds_slot;
    parts = &sNdsFighterPartsPool[slot][joint_id];
    translate = &dobj->translate.vec.f;
    scale = &dobj->scale.vec.f;

    ndsFighterPartsSetIdentity(parts->unk_dobjtrans_0x10);
    ndsFighterPartsSetIdentity(parts->mtx_translate);
    ndsFighterPartsSetIdentity(parts->unk_dobjtrans_0x9C);

    parts->transform_update_mode = 1;
    parts->unk_dobjtrans_0x5 = 1;
    parts->unk_dobjtrans_0x6 = 1;
    parts->unk_dobjtrans_0x7 = 1;
    parts->joint_id = (u8)joint_id;
    parts->gobj = fp->fighter_gobj;

    parts->vec_scale.x = (scale->x != 0.0F) ? scale->x : 1.0F;
    parts->vec_scale.y = (scale->y != 0.0F) ? scale->y : 1.0F;
    parts->vec_scale.z = (scale->z != 0.0F) ? scale->z : 1.0F;

    parts->unk_dobjtrans_0x10[0][0] = parts->vec_scale.x;
    parts->unk_dobjtrans_0x10[1][1] = parts->vec_scale.y;
    parts->unk_dobjtrans_0x10[2][2] = parts->vec_scale.z;
    parts->unk_dobjtrans_0x10[3][0] = translate->x;
    parts->unk_dobjtrans_0x10[3][1] = translate->y;
    parts->unk_dobjtrans_0x10[3][2] = translate->z;

    parts->mtx_translate[0][0] = parts->vec_scale.x;
    parts->mtx_translate[1][1] = parts->vec_scale.y;
    parts->mtx_translate[2][2] = parts->vec_scale.z;
    parts->mtx_translate[3][0] = translate->x;
    parts->mtx_translate[3][1] = translate->y;
    parts->mtx_translate[3][2] = translate->z;

    parts->unk_dobjtrans_0x9C[0][0] = 1.0F / parts->vec_scale.x;
    parts->unk_dobjtrans_0x9C[1][1] = 1.0F / parts->vec_scale.y;
    parts->unk_dobjtrans_0x9C[2][2] = 1.0F / parts->vec_scale.z;
    parts->unk_dobjtrans_0x9C[3][0] = -translate->x;
    parts->unk_dobjtrans_0x9C[3][1] = -translate->y;
    parts->unk_dobjtrans_0x9C[3][2] = -translate->z;

    dobj->parent_gobj = fp->fighter_gobj;
    if ((joint_id == nFTPartsJointTopN) && (dobj->parent == NULL))
    {
        dobj->parent = DOBJ_PARENT_NULL;
    }
    dobj->user_data.p = parts;
}

static void *ndsFighterLoadExternFile(const void *file_id)
{
    size_t size = lbRelocGetFileSize(file_id);
    void *heap;

    if (size == 0u)
    {
        gNdsFighterMarioFoxExternalFixupFailCount++;
        return NULL;
    }

    heap = syTaskmanMalloc(size, 0x10);
    if (heap == NULL)
    {
        gNdsFighterMarioFoxExternalFixupFailCount++;
        return NULL;
    }
    return lbRelocGetExternHeapFile(file_id, heap);
}

