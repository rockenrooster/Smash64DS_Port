static u32 ndsFighterStructPoolCount(void)
{
    u32 count = 0;
    u32 mask = sNdsFighterStructPoolUsedMask;

    while (mask != 0u)
    {
        count += mask & 1u;
        mask >>= 1;
    }
    return count;
}

static void ndsFighterStructPopulateJointsRecurse(FTStruct *fp, DObj *dobj,
                                                  u32 *next_joint,
                                                  sb32 is_root)
{
    while (dobj != NULL)
    {
        fp->nds_joint_count++;

        if (is_root == FALSE)
        {
            if (*next_joint < nFTPartsJointNumMax)
            {
                u32 joint_id = *next_joint;

                fp->joints[joint_id] = dobj;
                ndsFighterPartsSyncDObj(fp, dobj, joint_id);
                (*next_joint)++;
                fp->nds_common_joint_count++;
            }
        }
        if (dobj->child != NULL)
        {
            ndsFighterStructPopulateJointsRecurse(fp, dobj->child,
                                                  next_joint, FALSE);
        }
        dobj = dobj->sib_next;
        is_root = FALSE;
    }
}

static void ndsFighterStructAliasMissingJoint(FTStruct *fp, s32 joint_id)
{
    DObj *fallback;

    if ((fp == NULL) || (joint_id < 0) ||
        (joint_id >= nFTPartsJointNumMax) || (fp->joints[joint_id] != NULL))
    {
        return;
    }
    fallback = fp->joints[nFTPartsJointCommonStart];
    if (fallback == NULL)
    {
        fallback = fp->joints[nFTPartsJointTopN];
    }
    fp->joints[joint_id] = fallback;
}

static void ndsFighterStructPopulateJoints(FTStruct *fp, DObj *root_dobj)
{
    u32 next_joint = nFTPartsJointCommonStart;

    if (fp == NULL)
    {
        return;
    }
    fp->joints[nFTPartsJointTopN] = root_dobj;
    ndsFighterPartsSyncDObj(fp, root_dobj, nFTPartsJointTopN);
    fp->nds_joint_count = 0u;
    fp->nds_common_joint_count = 0u;
    ndsFighterStructPopulateJointsRecurse(fp, root_dobj, &next_joint, TRUE);
    if (fp->attr != NULL)
    {
        ndsFighterStructAliasMissingJoint(fp, fp->attr->joint_itemheavy_id);
    }
}

