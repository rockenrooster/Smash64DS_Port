static void ndsFTPhysicsApplyGroundVelFrictionBounded(GObj *fighter_gobj)
{
    FTStruct *fp = ftGetStruct(fighter_gobj);
    FTAttributes *attr;
    u32 material;
    f32 traction;
    f32 friction;

    if ((fp == NULL) || (ndsFighterStructIsPoolPointer(fp) == FALSE))
    {
        return;
    }
    attr = fp->attr;
    if (attr == NULL)
    {
        return;
    }

    material = fp->coll_data.floor_flags & MAP_VERTEX_MAT_MASK;
    material &= 0x0fu;
    traction = attr->traction;
    if (traction <= 0.0F)
    {
        traction = 1.0F;
    }
    friction = dMPCollisionMaterialFrictions[material] * traction;
    ndsFighterWaitGroundRecordMaterial(fp->player, material, traction,
                                       friction);

    ndsFighterSyncLegacyVelToPhysics(fp);
    ndsFTPhysicsSetGroundVelFrictionOriginal(fp, friction);
    ndsFTPhysicsSetGroundVelTransferAirOriginal(fighter_gobj, fp);
    ndsFighterSyncPhysicsToLegacyVel(fp);
}

